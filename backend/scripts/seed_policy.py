async def seed_once(name: str, category: str, content: str, upload_dir: Path, factory) -> str:
    """若同名+同分类文件已存在则跳过，否则写文件并入库。"""
    async with factory() as session:
        result = await session.execute(
            select(KnowledgeFile).where(
                KnowledgeFile.name == name,
                KnowledgeFile.category == category,
            )
        )
        if result.scalars().first() is not None:
            return f"[跳过] {category}/{name}（记录已存在）"

    upload_dir.mkdir(parents=True, exist_ok=True)
    file_path = upload_dir / name.replace(" ", "_")
    file_path.write_text(content, encoding="utf-8")

    async with factory() as session:
        file_id = await ingest_markdown_file(
            session,
            file_path=file_path,
            name=name,
            category=category,
            access_level="public",
        )
    return f"[成功] {category}/{name}  →  id={file_id}"


async def main() -> int:
    print("=== 制度万事通种子数据初始化 ===\n")
    init_engine()
    factory = get_session_factory()
    upload_dir = Path(settings.POLICY_UPLOAD_DIR)

    for doc in SEED_DOCS:
        try:
            msg = await seed_once(
                name=doc["name"],
                category=doc["category"],
                content=doc["content"],
                upload_dir=upload_dir,
                factory=factory,
            )
            print(msg)
        except Exception as exc:
            print(f"[失败] {doc['category']}/{doc['name']}：{exc}")

    await dispose_engine()
    print("\n=== 初始化完成 ===")
    return 0


